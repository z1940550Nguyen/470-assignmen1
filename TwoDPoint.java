/*******************************************************************************
Demonstrate a simple "2-D point" class, which is defined by x and y 
coordinations
*******************************************************************************/
public class TwoDPoint {
    public int x,y;

    /***************************************************************************
     * constructor a 2-D point
     * parameters: x, y
     * construct a 2-D point using x and y coordinations.
     **************************************************************************/
    public TwoDPoint (int x, int y)
    {
        this.x=x;
        this.y=y;
    }
}
